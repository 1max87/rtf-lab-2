/**
 * Напишите функцию sum, вычисляющую суммы подобным образом:
 * sum(1)(2)( ) // 3
 * sum(1)(2)(3)( ) // 6
 * sum :: Number -> sum
 * sum :: void -> Number
 */
function sum (x) {
    if (x === undefined) return 0;

    const sum = (total, current) => current === undefined ? total : sum.bind(null, total + current);

    return sum.bind(null, x);
}

module.exports = {
    customBind,
    sum
};
